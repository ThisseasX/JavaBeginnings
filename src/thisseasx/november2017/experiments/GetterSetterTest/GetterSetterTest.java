package thisseasx.november2017.experiments.GetterSetterTest;

public class GetterSetterTest {
    static class Dog {

        private String name;
        private int age;

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }
    }
}
